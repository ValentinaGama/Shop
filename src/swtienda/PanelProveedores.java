    private void agregaCliente()
    {
        boolean existeCodigo, existeNombre;
                
        this.jToggleButton2.setText("Nuevo");
        Proveedores prove=null;
        if (!(this.jTextField4.getText().equals("")) && !(this.jTextField3.getText().equals("")))
            {
                String ID = this.jTextField4.getText();
                String nombre = this.jTextField3.getText();
                existeCodigo=this.gestorProv.verificarID(ID);

              if(!existeCodigo)
              {
                 existeNombre=this.gestorProv.verificarNombre(nombre);
                 if(!existeNombre)
                 {

                  prove = new Proveedores(ID, nombre);

                  listaProv.add(prove);
                  this.gestorProv.recargArchivo(listaProv);
                 // this.comp.llenarcombos();
                  JOptionPane.showMessageDialog(null, "Proveedor a帽adido");
                  this.jTextField4.setText("");
                  this.jTextField3.setText("");
                 }
                 else
                 {
                  JOptionPane.showMessageDialog(null, "ESE NOMBRE YA EXISTE..!!!");
                  this.jTextField3.setText("");
                  this.jTextField3.requestFocus();
                 }
                }
               else
               {
                   JOptionPane.showMessageDialog(null, "ESE ID YA EXISTE..!!!"); 
                   this.jTextField3.setText("");
                   this.jTextField4.requestFocus();
               }
            
            }
        else
            {
              JOptionPane.showMessageDialog(null, "no ingreso todos los parametros \n"
                      + "cliente no a帽adido");

            }
        this.traeProductos(prove);
        
    }
    
    private void traeProductos(Proveedores clie)
    {
          Vector fila=new Vector();
          fila.add(clie.getID());
          fila.add(clie.getNombre());
          
          this.modelo.addRow(fila);
      
    }
    
    private void limpiar()
    {
        int a =modelo.getRowCount()-1;
        for(int i=a; i>=0; i--)
        {
            modelo.removeRow(i );
        }
    }
    
    private void modelaTabla()
    {
        this.jTable3.setModel(modelo);
        this.elQueOrdena= new TableRowSorter<TableModel>(this.jTable3.getModel());
        this.modelo.addColumn("ID");
        this.modelo.addColumn("Nombre");
        
        this.jTable3.setRowSorter(elQueOrdena);
        ArrayList <RowSorter.SortKey> SortKey=new ArrayList<>();
        
        elQueOrdena.setSortKeys(SortKey);
        elQueOrdena.sort();
    }
    
    private void traeTodos()
    {
      this.limpiar();
      
      for(Proveedores cli: this.listaProv)
      {
          Vector fila=new Vector();
          fila.add(cli.getID());
          fila.add(cli.getNombre());
          
          
          this.modelo.addRow(fila);
      }
    }
    
    private void tablaPone()
    {
        int seleccionada;
        String dato;
        seleccionada=this.jTable3.getSelectedRow();
        if(seleccionada>=0)
        {
            //hago la vuelta
            dato=this.jTable3.getValueAt(seleccionada,0).toString();
            this.jTextField3.setText(dato);
            dato=this.jTable3.getValueAt(seleccionada,1).toString();
            this.jTextField4.setText(dato);
            
        }
     
    }
    
